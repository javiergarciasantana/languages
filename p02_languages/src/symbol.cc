#include "symbol.h"

std::ostream& operator<<(std::ostream& out, const Symbol& symbol) {
  out << symbol.symbol_;
  return out;
}
bool Symbol::operator==(const Symbol& other_symbol) {
  if (symbol_ == other_symbol.symbol_) {
    return true;
  } else {
    return false;
  }
}